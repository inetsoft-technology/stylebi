CREATE TABLE INETSOFT_USER (
  USER_NAME VARCHAR(255) PRIMARY KEY,
  EMAIL VARCHAR(255),
  PASSWORD VARCHAR(255)
);

CREATE TABLE INETSOFT_ROLE (
  ROLE_NAME VARCHAR(255) PRIMARY KEY
);

CREATE TABLE INETSOFT_GROUP (
  GROUP_NAME VARCHAR(255) PRIMARY KEY
);

CREATE TABLE INETSOFT_USER_ROLE (
  USER_NAME VARCHAR(255) NOT NULL,
  ROLE_NAME VARCHAR(255) NOT NULL,
  PRIMARY KEY (USER_NAME, ROLE_NAME),
  FOREIGN KEY (USER_NAME) REFERENCES INETSOFT_USER (USER_NAME),
  FOREIGN KEY (ROLE_NAME) REFERENCES INETSOFT_ROLE (ROLE_NAME)
);

CREATE TABLE INETSOFT_GROUP_USER (
  GROUP_NAME VARCHAR(255) NOT NULL,
  USER_NAME VARCHAR(255) NOT NULL,
  PRIMARY KEY (GROUP_NAME, USER_NAME),
  FOREIGN KEY (GROUP_NAME) REFERENCES INETSOFT_GROUP (GROUP_NAME),
  FOREIGN KEY (USER_NAME) REFERENCES INETSOFT_USER (USER_NAME)
);

